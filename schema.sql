CREATE DATABASE clinic_DB;

CREATE TABLE patients(
  id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name VARCHAR(100),
  date_of_birth date
)

CREATE TABLE medical_histories(
  id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  admitted_at DATE,
  patient_id INT,
  status VARCHAR(80)
)

CREATE TABLE treatments (
  id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  type VARCHAR(80),
  name VARCHAR(100)
)
